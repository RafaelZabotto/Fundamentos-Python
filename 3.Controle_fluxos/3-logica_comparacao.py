# Operadores logicos

idade = 15

print(idade > 17) #False
print(idade < 17) #True
print(idade <= 17) #True
print(idade >= 17) #False
print(idade == 17) #False
print(idade != 17) #True

# Comparações entre outros tipos

print(True == True) # True
print('Rafael' == 'rafael') # False
print('b' > 'a')
print(5 == '5')
