# Laço de repetição

# Como de costume, inicia-se no 0 e vai até 4 neste caso
for numero in range(5):
    print('carregando', numero)

# Exemplo do 1 ao 21, mas pulando de 2 em 2

for numero in range(1, 21, 2):
    print('carregando ', numero) 

